/**
 * Changes the website language
 * @param {string} lang - 'ar', 'en', or 'fr'
 */
function changeLanguage(lang) {
    if (!translations[lang]) return;

    // Save selection
    localStorage.setItem('preferredLanguage', lang);

    // Update HTML attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    // Update all elements with data-lang
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[lang][key]) {
            // Special handling for meta titles and inputs
            if (element.tagName === 'TITLE') {
                document.title = translations[lang][key];
            } else if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                if (key.endsWith('_placeholder')) {
                    element.placeholder = translations[lang][key];
                } else {
                    element.value = translations[lang][key];
                }
            } else {
                // Check if element has an emoji span we want to preserve
                const iconSpan = element.querySelector('span:first-child');
                if (iconSpan && iconSpan.textContent.length <= 4 && !translations[lang][key].includes('<span')) { 
                    // Preserve the icon and replace the rest
                    const icon = iconSpan.outerHTML;
                    element.innerHTML = icon + ' ' + translations[lang][key];
                } else if (translations[lang][key].includes('<span')) {
                    // If translation itself contains HTML (for specific styling)
                    element.innerHTML = translations[lang][key];
                } else {
                    element.textContent = translations[lang][key];
                }
            }
        }
    });

    // Special fallback for title if not handled by [data-lang]
    if (!document.querySelector('title[data-lang]')) {
        document.title = translations[lang]['title'];
    }

    // Update active state in UI if needed (logic can be added here)
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'ar';
    changeLanguage(savedLang);
});

